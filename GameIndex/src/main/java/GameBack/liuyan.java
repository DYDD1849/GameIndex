package GameBack;

public class liuyan {
	private	String liuyanRen;
	private	String liuyantime;
	private String gamename;
	private	String liuyan;
	
	
	
	public liuyan(String liuyanRen, String liuyantime, String gamename, String liuyan) {
		super();
		this.liuyanRen = liuyanRen;
		this.liuyantime = liuyantime;
		this.gamename = gamename;
		this.liuyan = liuyan;
	}
	public String getLiuyanRen() {
		return liuyanRen;
	}
	public void setLiuyanRen(String liuyanRen) {
		this.liuyanRen = liuyanRen;
	}
	public String getLiuyantime() {
		return liuyantime;
	}
	public void setLiuyantime(String liuyantime) {
		this.liuyantime = liuyantime;
	}
	public String getGamename() {
		return gamename;
	}
	public void setGamename(String gamename) {
		this.gamename = gamename;
	}
	public String getLiuyan() {
		return liuyan;
	}
	public void setLiuyan(String liuyan) {
		this.liuyan = liuyan;
	}
	
	
		
}
